// Content calendar generator
export const generateContentCalendar = (month, year) => {
  const calendar = [];
  const daysInMonth = new Date(year, month + 1, 0).getDate();
  
  // Weekly posts on Monday, Wednesday, Friday, Sunday
  for (let day = 1; day <= daysInMonth; day++) {
    const date = new Date(year, month, day);
    const dayOfWeek = date.getDay();
    
    if (dayOfWeek === 1) { // Monday - What's New
      calendar.push({
        date: date.toISOString().split('T')[0],
        type: 'whats_new',
        template: GBP_POSTS.weekly[1],
      });
    } else if (dayOfWeek === 3) { // Wednesday - Offer
      calendar.push({
        date: date.toISOString().split('T')[0],
        type: 'offer',
        template: GBP_POSTS.weekly[0],
      });
    } else if (dayOfWeek === 5) { // Friday - Event/Local
      calendar.push({
        date: date.toISOString().split('T')[0],
        type: 'event',
        template: GBP_POSTS.weekly[3],
      });
    } else if (dayOfWeek === 0) { // Sunday - Photo
      calendar.push({
        date: date.toISOString().split('T')[0],
        type: 'photo',
        template: GBP_POSTS.weekly[2],
      });
    }
  }
  
  return calendar;
};

